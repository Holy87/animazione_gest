    <h1>Guida e Aiuto</h1>
    <hr>
    <h2>Login</h2>
    <h3>Non riesco ad entrare, mi dice nome utente o password errata</h3>
    <p>Probabilmente hai dimenticato la password o non ne possiedi una. Contatta un amministratore dell'applicazione per risolvere il problema.</p>
    <h3>Riesco ad entrare, ma non vedo le schede!</h3>
    <p>Sei stato bloccato da un amministratore. Contattalo per sapere altro sui motivi del blocco.</p>
    <h3>Posso recuperare la password in qualche modo?</h3>
    <p>Al momento solo l'amministratore può darti una nuova paasword.</p>
    <hr>
    <h2>Account</h2>
    <h3>Come posso cambiare le informazioni sul mio profilo?</h3>
    <p>Clicca sul tuo nome nella barra di navigazione in alto a destra, e quindi su <a href="profilo">profilo</a>. Puoi modificare la foto del profilo, la mail di contatto e la password.</p>
    <h3>Posso cambiare il mio nome?</h3>
    <p>Solo gli amministratori possono cambiare il nome. Questo per prevenire confusioni. Se ritieni che il tuo nome sia errato, contatta un amministratore.</p>
    <h3>Posso cambiare nome utente (di login)?</h3>
    <p>Al momento non è possibile cambiare username. Questo è stato fatto per prevenire eventuali incongruenze.</p>
    <h3>Perché non posso eliminare un utente anche se sono amministratore?</h3>
    <p>Non puoi eliminare un utente se è il tuo utente collegato: c'è bisogno che esista almeno un amministratore.</p>
    <p>Non puoi eliminare un utente che ha partecipato o parteciperà ad almeno una festa: nel caso vorrai eliminarlo, bisognerà rimuovere l'utente da tutte le feste.</p>
    <hr>
    <h2>Ruoli</h2>
    <h3>Cosa sono i ruoli?</h3>
    <p>I ruoli permettono di assegnare determinati diritti e facoltà agli utenti registrati.</p>
    <ul>
        <li><strong>Animatore</strong>: Un animatore semplice. Può:
        <ul>
            <li>Visualizzare le feste a cui è stato assegnato</li>
        </ul></li>
        <li><strong>Segreteria</strong>: ha più diritti rispetto all'animatore perché deve anche gestire le chiamate. Può:
        <ul>
            <li>Visualizzare, creare e modificare tutte le feste</li>
            <li>Visualizzare l'inventario</li>
            <li>Aggiungere, rimuovere e modificare oggetti nell'inventario</li>
        </ul></li>
        <li><strong>Amministratore</strong>: Ha poteri completi sull'applicazione. Rispetto alla segreteria, ha in più i poteri di:
        <ul>
            <li>Creare, modificare ed eliminare utenti</li>
            <li>Modificare i nomi degli utenti</li>
        </ul></li>
        <li><strong>Bloccato</strong>: non può far nulla.</li>
    </ul>
    <h3>Chi può cambiare ruolo?</h3>
    <p>Solo gli amministratori, attraverso il pannello <a href="users">utenti</a>.</p>
</div>